const a = 10;
const b = 5;

function adicao() {
    return a + b;
}

function subtracao() {
    return a - b;
}

function multiplicacao() {
    return a * b;
}

function divisao () {
    return a / b;
}

function modulo() {
    return a % b;
}

console.log(adicao());
console.log(subtracao());
console.log(multiplicacao());
console.log(divisao());
console.log(modulo());